# What is Variable?
# A variable is a container for storing data values. It is a way to give a name
name = "Basant"
# We can use this variable to store any type of data, such as numbers, strings, lists, etc.
# Here name is a variable and "Basant" is the value stored in that variable.
# We can use this variable to access the value stored in it and perform operations on it.
# We can have multiple variables
city = "Reengus"
goal = "Build My Own AI Assistant"
print(name)
print(city)
print(goal)
# We can use this in a sentance
print("My name is", name, "and I live in", city, "and my goal is to", goal)
# Python stores Different types of data the import ones are:
# String , Integer, Float, Boolean
# String is a sequence of characters and it is enclosed in quotes
name = "Basant"
# Integer is a whole number without a decimal point    
age = 18
# Float is a number with a decimal point
height = 5.11
# Boolean is a data type that can have only two values: True or False
student = True

# We can use the type() function to check the type of a variable
print(type(name))
print(type(age))
print(type(height))
print(type(student))
print(type(city))
print(type(goal))

# We can also use variable to do some calculation
a = 10
b = 5
sum = a + b
difference = a - b
product = a * b
quotient = a / b
print("Sum:", sum)
print("Difference:", difference)
print("Product:", product)
print("Quotient:", quotient)

# We can also  update variables
a = 10
print("Value of a before update:", a)
a = 20
print("Value of a after update:", a)

